
public class Draw {

	protected char appearance;
	protected int height;
	protected int width;

	public void setAppearance(char a)
	{
		appearance = a;
	}
	
	public char getAppearance()
	{
		return appearance;
	}
	
	public void setHeight(int h)
	{
		height = h;
	}
	
	public int getHeight()
	{
		return height;
	}
	
	public void setWidth(int w)
	{
		width = w;
	}
	
	public int getWidth()
	{
		return width;
	}
	
}
